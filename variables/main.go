package main

import (
	"fmt"
)

func main() {
	var myIntVar int = -12
	fmt.Println(myIntVar)

	var myUintVar uint = 12
	fmt.Println(myUintVar)

	var myStryng string = "Esto es una cadena de texto"
	fmt.Println(myStryng)

	var myBoolVar bool = true
	fmt.Println(myBoolVar)
	fmt.Println(&myBoolVar)

}
